string = "toclab"
print("The string is " + string)

 # first character of string
print("First character\t\t\t:\t" + string[0])

# last character of string
print("Last character\t\t\t:\t" + string[-1])

# last 2 character of string
print("Last 2 characters\t\t:\t" + string[-2])

# character with range of string
print("Sub-string \t\t\t:\t" + string[1:3])

# all character i.e. first to last
print("All character of string\t\t:\t" + string[:])

# + concatenation
print("Concatenation of sub-string\t:\t" + string[0]+string[-1])